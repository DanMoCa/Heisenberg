    private void jBtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSaveActionPerformed
        // TODO add your handling code here:
        try {
            //"idHerramienta, idInventario_Uach,Nombre,Cantidad,Observaciones_Generales,Condicion";
            String sql = "UPDATE Herramienta SET "
                    + "idHerramienta = ?, "
                    + "idInventario_Uach = ?, "
                    + "Cantidad = ?, "
                    + "Nombre = ?, "
                    + "Condicion = ?, "
                    + "Observaciones_Generales = ?, "
                    + "CategoriaHerramienta_idCategoriaHerramienta = ? "
                    + "WHERE idHerramienta = ? ";

            Connection conn = Conexion.GetConnection();
            conn.setAutoCommit(false);
            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, jTxtFldId.getText());
            ps.setString(2, jTxtFldUach.getText());
            ps.setInt(3, Integer.parseInt(jTxtFldCant.getText()));
            ps.setString(4, jTxtFldNombre.getText());
            ps.setString(5, jCBCond.getSelectedItem().toString());
            ps.setString(6, jTxtDesc.getText());
            ps.setInt(7, jCBCat.getSelectedIndex() + 1);
            ps.setString(8, jTxtFldId.getText());
            
            ps.executeUpdate();
            
            conn.commit();
            
            cargarHerramientas();
            clearFields();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jBtnSaveActionPerformed

